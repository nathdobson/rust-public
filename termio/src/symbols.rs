pub fn negative_circled_number(x: usize) -> Option<&'static str> {
    Some(match x {
        0 => "⓿",
        1 => "➊",
        2 => "➋",
        3 => "➌",
        4 => "➍",
        5 => "➎",
        6 => "➏",
        7 => "➐",
        8 => "➑",
        9 => "➒",
        10 => "➓",
        11 => "⓫",
        12 => "⓬",
        13 => "⓭",
        14 => "⓮",
        15 => "⓯",
        16 => "⓰",
        17 => "⓱",
        18 => "⓲",
        19 => "⓳",
        20 => "⓴",
        _ => return None,
    })
}